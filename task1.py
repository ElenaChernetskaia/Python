a = int(input("Введите стартовую длину забега >>> "))
b = int(input("Введите цель по длине забега >>> "))
day = 1
while a <= b:
    print(day, "-й день: ", a, "км")
    a *= 1.1
    day += 1
else:
    print(day, "-й день: ", a, "км")
    print("Поздравляю! Вы достигли цели на ", day, "день.")
